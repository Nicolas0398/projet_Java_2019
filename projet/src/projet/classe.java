    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            model.setRowCount(0);
            {
                rset = stmt.executeQuery("Select * From classe WHERE nom = '"+txtrech.getText()+"'");
            }while(rset.next()){
                Object[] classe = {rset.getString(1),rset.getString(2),rset.getString(3),rset.getString(4),rset.getString(5)};
                model.addRow(classe);
            }

            if (model.getRowCount()==0){
                JOptionPane.showMessageDialog(null,"Il n'y a pas de classe correspondant a votre recherche");
            }

        }catch(Exception e){JOptionPane.showMessageDialog(null,"erreur de recherche");}
    }//GEN-LAST:event_jButton4ActionPerformed

