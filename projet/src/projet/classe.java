    private void deplace (int i){
    try{
      txtc.setText(model.getValueAt(i,0).toString());  
      txtn.setText(model.getValueAt(i,1).toString()); 
      txte.setSelectedItem(model.getValueAt(i,2).toString()); 
      txtni.setSelectedItem(model.getValueAt(i,3).toString()); 
      txti.setSelectedItem(model.getValueAt(i,4).toString()); 
      
    }catch (Exception e){System.err.println(e);
    JOptionPane.showMessageDialog(null, "erreur de deplacement"+e.getLocalizedMessage());}
}
