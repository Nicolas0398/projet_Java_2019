    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if(txtidniv.getText().length() != 0){
                stmt.executeUpdate("Delete From niveau where niveauID ="+txtidniv.getText());
                JOptionPane.showMessageDialog(null,"Le niveau a bien été supprimé");
            }
            else{JOptionPane.showMessageDialog(null,"Veuillez remplir le champ : ");}
        }catch(Exception e){JOptionPane.showMessageDialog(null,"erreur dans la suppression");}
    }//GEN-LAST:event_jButton2ActionPerformed

